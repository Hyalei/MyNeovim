vim.opt.number = true
vim.opt.relativenumber = true

vim.opt.cursorline = true
vim.opt.termguicolors = true


vim.opt.tabstop = 4       -- 一个 Tab 字符显示为4个空格宽度
vim.opt.softtabstop = 4   -- 插入模式下，按一次 Tab 键等于4个空格
vim.opt.shiftwidth = 4    -- 自动缩进时使用4个空格宽度
vim.opt.expandtab = true  -- 按 Tab 键时插入空格而不是 Tab 字符

vim.opt.autoread =  true
vim.opt.clipboard = "unnamedplus"

vim.opt.splitbelow = true  -- 水平分屏方向默认向下
vim.opt.splitright = true  -- 垂直分屏方向默认向右
